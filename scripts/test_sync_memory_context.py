"""Tests for scripts/sync_memory_context.py."""

from __future__ import annotations

import importlib.util
from pathlib import Path

import pytest

_SCRIPT_DIR = Path(__file__).resolve().parent
_MODULE_PATH = _SCRIPT_DIR / "sync_memory_context.py"

_SPEC = importlib.util.spec_from_file_location("sync_memory_context", _MODULE_PATH)
assert _SPEC is not None and _SPEC.loader is not None
_smc = importlib.util.module_from_spec(_SPEC)
_SPEC.loader.exec_module(_smc)  # type: ignore[union-attr]


_SAMPLE_SESSION = """# Session Memory

## Current Objective
- Evolve the orchestration repo with Zeus as root conductor.

## Active Repositories
- review_clones/OrchestationAgentsSkillLike

## In-Flight Decisions
- Memory continuity hook work is in progress.

## Current Batch
- Memory hook implementation in progress.

## Next Likely Moves
- Run tests, update README, commit.

## Blockers
- None currently documented.
"""

_SAMPLE_DECISIONS = """# Decision Log

| Date | Scope | Decision | Rationale | Consequence |
|------|-------|----------|-----------|-------------|
| 2026-03-22 | Memory | Start file-backed memory | Low risk | No DB dependency |
| 2026-03-25 | Hook | Memory continuity hook | Deterministic | zeus-context.md refreshed |
"""


def test_build_context_includes_current_objective() -> None:
    result = _smc.build_context(_SAMPLE_SESSION, _SAMPLE_DECISIONS)
    assert "Current Objective" in result
    assert "Zeus as root conductor" in result


def test_build_context_includes_blockers() -> None:
    result = _smc.build_context(_SAMPLE_SESSION, _SAMPLE_DECISIONS)
    assert "Blockers" in result
    assert "None currently documented" in result


def test_build_context_includes_next_likely_moves() -> None:
    result = _smc.build_context(_SAMPLE_SESSION, _SAMPLE_DECISIONS)
    assert "Next Likely Moves" in result
    assert "update README" in result


def test_build_context_includes_recent_decisions() -> None:
    result = _smc.build_context(_SAMPLE_SESSION, _SAMPLE_DECISIONS)
    assert "Recent Decisions" in result
    assert "2026-03-22" in result
    assert "2026-03-25" in result


def test_build_context_includes_auto_generated_comment() -> None:
    result = _smc.build_context(_SAMPLE_SESSION, _SAMPLE_DECISIONS)
