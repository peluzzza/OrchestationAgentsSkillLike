# ---------------------------------------------------------------------------

def test_main_returns_zero_and_writes_output_file(tmp_path: Path, capsys) -> None:
    memory_dir = tmp_path / ".specify" / "memory"
    memory_dir.mkdir(parents=True)
    (memory_dir / "session-memory.md").write_text(_SAMPLE_SESSION, encoding="utf-8")
    (memory_dir / "decision-log.md").write_text(_SAMPLE_DECISIONS, encoding="utf-8")

    exit_code = _smc.main(["--repo-root", str(tmp_path)])

    assert exit_code == 0
    output_path = memory_dir / "zeus-context.md"
    assert output_path.exists()
    captured = capsys.readouterr()
    assert "zeus-context refreshed" in captured.out
